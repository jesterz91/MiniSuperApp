import UIKit

extension UIImage {

    static let house: UIImage = .init(systemName: "house")!
    static let houseFill: UIImage = .init(systemName: "house.fill")!

    static let creditcard: UIImage = .init(systemName: "creditcard")!
    static let creditcardFill: UIImage = .init(systemName: "creditcard.fill")!

    static let person: UIImage = .init(systemName: "person")!
    static let personFill: UIImage = .init(systemName: "person.fill")!
}
